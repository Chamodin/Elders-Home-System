    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
         
        int id = Integer.parseInt(txtID.getText());
        String name = txtName.getText();
        String nic = txtNIC.getText();
        String address = txtAddress.getText();
        int contactNo = Integer.parseInt(txtContactNo.getText());
        String position = txtPosition.getText();
        int bedNo = Integer.parseInt(txtBedNo.getText());
        
        if (model.saveStaff(id, name, nic, address, contactNo, position, bedNo)) {
            JOptionPane.showMessageDialog(null, "Data Saved!!");
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong!!");
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

    
        int id = Integer.parseInt(txtID.getText());
        String nic = txtNIC.getText();
        String address = txtAddress.getText();
        int contactNo = Integer.parseInt(txtContactNo.getText());
        int bedNo = Integer.parseInt(txtBedNo.getText());
        String position = txtPosition.getText();

        if (model.updateStaff(id, nic, address, contactNo, position , bedNo)) {
            JOptionPane.showMessageDialog(null, "Data Updated!!");
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong!!");
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

