    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int id = Integer.parseInt(txtID.getText());

        if (model.deleteStaff(id)) {
            JOptionPane.showMessageDialog(null, "Data Deleted!!");
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
       
        int id = Integer.parseInt(txtID.getText());

        String[] details = model.viewStaff(id);

        if (details != null) {
            
            txtName.setText(details[1]);
            txtNIC.setText(details[2]);
            txtAddress.setText(details[3]);
            txtContactNo.setText(details[4]);
            txtBedNo.setText(details[5]);
            txtPosition.setText(details[6]);
        } else {
            JOptionPane.showMessageDialog(null, "Staff not found!!");
        }
        
        
    }//GEN-LAST:event_btnViewActionPerformed

